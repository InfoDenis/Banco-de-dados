1 CARACTER = 1 BYTE
CHAR -> NÃO VARIA O TAMANHO MAS É MAIS PERFORMÁTICO
VARCHAR -> VARIA O TAMANHO MAS É MAIS LENTO

INT -> NUMEROS INTEIROS -> MÁXIMO 11 DIGITOS POR ISSO AS VEZES CADASTRA-SE COMO VARCHAR EM ALGUNS CAMPOS NUMÉRICOS
FLOAT -> NÚMEROS REAIS FLOAT(TOTAL DE CASAS, CASAS DEPOIS DA VÍRGULA)

ENUM -> ENUMERAÇÃO -> É UM CONJUNTO DE DADOS ENUMERADO, OU SEJA, É UM NÚMERO FIXO DE DADOS -> ULTILIZAMOS 
